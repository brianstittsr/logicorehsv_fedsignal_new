import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { accent?: 'radar' | 'green' | 'amber' | 'red' | 'uni' }
>(({ className, accent, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border bg-card text-card-foreground shadow-sm overflow-hidden relative",
      className
    )}
    {...props}
  >
    {accent && (
      <div 
        className="absolute top-0 left-0 right-0 h-[3px]"
        style={{
          background: accent === 'uni' 
            ? 'linear-gradient(90deg, var(--uni-primary), var(--uni-secondary))'
            : `var(--${accent === 'radar' ? 'radar' : accent})`
        }}
      />
    )}
    <div className={cn(accent && "pt-3")}>
      {props.children}
    </div>
  </div>
))
Card.displayName = "Card"

